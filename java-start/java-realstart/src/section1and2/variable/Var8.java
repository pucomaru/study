package section1and2.variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32,768 ~ 32,767
        int i = 2147483647; //-2,147,483,648 ~ 2,147,483,657 (약 20억)

        long l = 23123123123333335L ; // 엄청 김 Long 뒤 소문자보다는 대문자 쓰기 L
        //높을수록 메모리 공간 많이 차지함

        //실수
        float f = 10.0f;
        double d = 10.0;

        //정수는 int 실수 double 기본으로 씀
        //float 거의 안씀 실무에서
    }
}
