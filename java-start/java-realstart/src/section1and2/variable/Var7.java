package section1and2.variable;

public class Var7 {
    public static void main(String[] args) {
        int a= 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; //불리언(boolean) true, falese 입력 가능
        char d = 'A'; //문자 하나
        String e = "Hello Java"; //문자열, 문자열을 다루기 위한 특별한 타입

        System.out.println(a); //ctrl + D 단축기 바로 봇가됨
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        // 코드에서 개발자가 직접적은 100, 10.5 , true 와 같은 것을 리터럴이러함
    }
}
